            Exp.Colbar.YLabel.Units = 'points';
            Exp.Colbar.YLabel.Position = [15 (Size(2))/2];
                      
    end
elseif Key>0 && Key <=6
   %% ROI selection mouse clicks
   switch Type
       case 'normal' %%% Rectangular ROI selection
