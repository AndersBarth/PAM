    switch PhasorData.Type
        case 1
            h.Phasor_Plot.XLim = [-0.1 1.2];
            h.Phasor_Plot.YLim = [-0.1 1.2];
        case 2
            h.Phasor_Plot.XLim = [-1 1];
            h.Phasor_Plot.YLim = [-1 1];
    end
end

